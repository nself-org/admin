async function checkEnvSecretsIgnored(root: string): Promise<AuditItem> {
  try {
    const raw = await fs.readFile(path.join(root, '.gitignore'), 'utf-8')
    const ok = /\.env(\.secrets|\.\*)?/.test(raw)
    return {
      id: 'env-secrets-ignored',
      category: 'Security-Always-Free',
      title: '.env.secrets is gitignored',
      description: 'Secrets must never be committed.',
      status: ok ? 'pass' : 'fail',
    }
  } catch {
    return {
      id: 'env-secrets-ignored',
      category: 'Security-Always-Free',
      title: '.env.secrets is gitignored',
      description: 'Secrets must never be committed.',
      status: 'fail',
      detail: 'No .gitignore found.',
    }
  }
}

async function checkSslCertsPresent(root: string): Promise<AuditItem> {
  const candidates = ['ssl/certs', 'nginx/ssl', '.ssl']
  for (const c of candidates) {
    try {
      await fs.access(path.join(root, c))
      return {
        id: 'ssl-certs',
        category: 'Security-Always-Free',
        title: 'SSL certificates present',
        description: 'Local dev TLS is enabled.',
        status: 'pass',
        detail: `Found at ${c}`,
      }
    } catch {
      // keep trying
    }
  }
  return {
    id: 'ssl-certs',
    category: 'Security-Always-Free',
    title: 'SSL certificates present',
    description: 'Local dev TLS is enabled.',
    status: 'warn',
    detail: 'No SSL directory found — run `nself ssl` to generate.',
  }
}

