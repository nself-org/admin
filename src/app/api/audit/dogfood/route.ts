import { getActiveProject } from '@/features/project-picker/project-picker'
import fs from 'fs/promises'
import { NextResponse } from 'next/server'
import path from 'path'

interface AuditItem {
  id: string
  category: string
  title: string
  description: string
  status: 'pass' | 'fail' | 'warn' | 'unknown'
  detail?: string
}

/**
 * Run the nSelf dog-food audit against the currently active project.
 *
 * Categories:
 *   - nSelf-First Doctrine  — compose is generated, no side-channel services
 *   - Doc-Sync Ritual       — SPORT exists, doc-sync workflow present
 *   - Security-Always-Free  — .env.secrets not committed, SSL enabled
 */
export async function GET() {
  try {
    const project = await getActiveProject()
    const items: AuditItem[] = []

    if (project === null) {
      items.push({
        id: 'project-missing',
        category: 'Setup',
        title: 'Active project selected',
        description: 'The audit requires a registered active project.',
        status: 'fail',
        detail: 'No active project — use the project picker to select one.',
      })
      return NextResponse.json(summarise(items))
    }

    // ---- nSelf-First Doctrine ---------------------------------------------
    items.push(await checkComposeGenerated(project.path))
    items.push(await checkNoSideChannelCompose(project.path))
    items.push(await checkEnvComputedPresent(project.path))

    // ---- Doc-Sync Ritual --------------------------------------------------
    items.push(await checkSportExists(project.path))
    items.push(await checkDocSyncWorkflow(project.path))
    items.push(await checkReadmePresent(project.path))

    // ---- Security-Always-Free --------------------------------------------
    items.push(await checkEnvSecretsIgnored(project.path))
    items.push(await checkSslCertsPresent(project.path))
    items.push(await checkGitignorePresent(project.path))

    return NextResponse.json(summarise(items))
  } catch (err) {
    const msg = err instanceof Error ? err.message : 'Audit failed.'
    return NextResponse.json({ error: msg }, { status: 500 })
  }
}

function summarise(items: AuditItem[]) {
  const score = { pass: 0, fail: 0, warn: 0, total: items.length }
  for (const it of items) {
    if (it.status === 'pass') score.pass += 1
    else if (it.status === 'fail') score.fail += 1
    else if (it.status === 'warn') score.warn += 1
  }
  return {
    generatedAt: new Date().toISOString(),
    items,
    score,
  }
}

async function checkComposeGenerated(root: string): Promise<AuditItem> {
  try {
    const raw = await fs.readFile(
      path.join(root, 'docker-compose.yml'),
      'utf-8',
    )
    const ok = raw.includes('GENERATED BY nself build')
    return {
      id: 'compose-generated',
      category: 'nSelf-First Doctrine',
      title: 'docker-compose.yml is CLI-generated',
      description:
        'The compose file must carry the GENERATED BY nself build header.',
      status: ok ? 'pass' : 'warn',
      detail: ok ? undefined : 'Header missing — may be hand-edited.',
    }
  } catch {
    return {
      id: 'compose-generated',
      category: 'nSelf-First Doctrine',
      title: 'docker-compose.yml is CLI-generated',
      description:
        'The compose file must carry the GENERATED BY nself build header.',
      status: 'warn',
      detail: 'No docker-compose.yml yet — run `nself build`.',
    }
  }
}

async function checkNoSideChannelCompose(root: string): Promise<AuditItem> {
  const sideFiles = ['docker-compose.prod.yml', 'docker-compose.custom.yml']
  const found: string[] = []
  for (const f of sideFiles) {
    try {
      await fs.access(path.join(root, f))
      found.push(f)
    } catch {
      // ignore
    }
  }
  return {
    id: 'no-side-channel',
    category: 'nSelf-First Doctrine',
    title: 'No side-channel compose files',
    description:
      'All services go through nself build. No hand-maintained compose variants.',
    status: found.length === 0 ? 'pass' : 'warn',
    detail: found.length === 0 ? undefined : `Found: ${found.join(', ')}`,
  }
}

