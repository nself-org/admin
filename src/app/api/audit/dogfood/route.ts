async function checkSportExists(root: string): Promise<AuditItem> {
  try {
    const stat = await fs.stat(path.join(root, '.claude/docs/sport'))
    return {
      id: 'sport-exists',
      category: 'Doc-Sync Ritual',
      title: 'SPORT directory present',
      description: '.claude/docs/sport/ holds canonical reference facts.',
      status: stat.isDirectory() ? 'pass' : 'fail',
    }
  } catch {
    return {
      id: 'sport-exists',
      category: 'Doc-Sync Ritual',
      title: 'SPORT directory present',
      description: '.claude/docs/sport/ holds canonical reference facts.',
      status: 'warn',
      detail:
        'Not present — nSelf PPI-level only; ignore if this is a consumer project.',
    }
  }
}

